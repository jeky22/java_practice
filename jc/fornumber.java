public class fornumber {
    public static void main(String[] args) {

        int i, j;
        for (i = 1; i < 6; i++) {
            for (j = 0; j < 5; j++) {

                System.out.print(i+j*5+" ");
            }
            System.out.println("\n");
        }
        
        System.out.println("-----------------------------");

    }
}